def add(a, b):
  return a + b
def subtract(a, b):
  return a - b
def division(a, b):
  return a/b
def multiplication(a, b):
  return a*b
  
print("Addition:", add(5, 3))
print("Subtraction:", subtract(5, 3))
print("Division:", division(5, 3))
print("Multiplicaiton", multiplication(5, 3))
